export function normalizeAppLang(input) {
  const raw = (input || "").toLowerCase();
  if (!raw) return "en";
  const short = raw.split("-")[0];
  return APP_LANGS.includes(short) ? short : "en";
}

export function getAppT(language = "en") {
  const lang = normalizeAppLang(language);
  return APP_I18N[lang] || APP_I18N.en;
}