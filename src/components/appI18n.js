function createTranslator(base, overrides = {}) {
  const merged = { ...base, ...overrides };
  return new Proxy(merged, {
    get(target, prop) {
      if (prop in target) return target[prop];
      return typeof prop === "string" ? base[prop] ?? prop : "";
    },
  });
}

