export const APP_I18N = {
  en: createTranslator(EN),
  ar: createTranslator(EN, AR),
  fr: createTranslator(EN, FR),
  es: createTranslator(EN, ES),
  de: createTranslator(EN, DE),
  tr: createTranslator(EN, TR),
  ur: createTranslator(EN, UR),
  id: createTranslator(EN, ID),
  pt: createTranslator(EN, PT),
  zh: createTranslator(EN, ZH),
};

export function normalizeAppLang(input) {
  const raw = (input || "").toLowerCase();
  if (!raw) return "en";
  const short = raw.split("-")[0];
  return APP_LANGS.includes(short) ? short : "en";
}

export function getAppDir(language = "en") {
  const lang = normalizeAppLang(language);
  // return ["ar", "ur"].includes(lang) ? "rtl" : "ltr";
  return ["ar", "ur"].includes(lang) ? "ltr" : "ltr";

}

export function getAppT(language = "en") {
  const lang = normalizeAppLang(language);
  return APP_I18N[lang] || APP_I18N.en;
}